package com.saganenko.loftmoney.remote.models;

public class MoneyItemResponse {
    private String date;
    private String id;
    private String name;
    private int price;
    private String type;

    public String getDate() {
        return date;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }
}
